package fxExam;

public class TableData {
	private String sub;
	private int score;
	
	public TableData(String sub, int score) {
		this.sub = sub;
		this.score = score;
	}

	public String getSub() {
		return sub;
	}

	public void setSub(String sub) {
		this.sub = sub;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}


	
}
