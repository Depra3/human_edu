package J221121;


public class Exam01 {
	public static void main(String[] args) {
		int a=0;
		for (int i = 1; i <= 100; i++) {
			a = a + i;
		}
		System.out.println(a);
	}
	/*
	 * 객체 지향 특징
	 * 
	 * 1. 캡슐화(Encapsuliztion)
	 * class, 접근제한자(private,public,default,protected)
	 * 
	 * 2. 상속(Inheritance)
	 * extends, implements
	 * overriding
	 * 
	 * 3. 다형성(Polymophism)
	 * 생성자 오버로딩
	 * 메소드 오버로딩
	 * 
	 * 4. 추상화(Abstraction)
	 * 추상 클래스(Abstract class),Interface
	 */
}
